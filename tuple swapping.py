#making a tuple
tuple=('Germany','Armenia','England')
#unpacking the tuple
p1, p2, p3 = tuple
print('First country in tuple is;',p1)
print('Second country in tuple is;',p2)
print('Third country in tuple is;',p3)
#swaping the values using tuple assignment
p1,p2,p3=p2,p3,p1
print('After swaping the values the tuple is;',p1,p2,p3)